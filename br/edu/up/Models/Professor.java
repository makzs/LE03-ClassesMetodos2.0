package br.edu.up.Models;

public class Professor {
    private int aulas;
    private int horas;
    private int nivel;
    public int getAulas() {
        return aulas;
    }
    public void setAulas(int aulas) {
        this.aulas = aulas;
    }
    public int getHoras() {
        return horas;
    }
    public void setHoras(int horas) {
        this.horas = horas;
    }
    public int getNivel() {
        return nivel;
    }
    public void setNivel(int nivel) {
        this.nivel = nivel;
    }

    
}
